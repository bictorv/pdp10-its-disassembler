#include <stdio.h>

#include "dis.h"

static void
write_octet_word (FILE *f, word_t word)
{
  fputc (word & 0377, f);
}

struct word_format octet_word_format = {
  "octet",
  NULL,
  NULL,
  write_octet_word,
  NULL
};
